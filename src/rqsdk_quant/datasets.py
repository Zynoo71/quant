def describe_dataset(name: str) -> dict[str, Any]:
    spec = get_dataset(name)
    note = "未列出的 rqdatac 参数可用 --param KEY=VALUE 或 --params '<json>' 传入；多值参数用空格或逗号分隔。"
    finder = _DISCOVERY_HINT.get(spec.category)
    if finder and "ids" in spec.param_map:
        note += f" 合约代码会到期，示例仅示形，先用 {finder} 查在市代码。"
    return {
        "name": spec.name,
        "category": spec.category,
        "module": spec.module,
        "function": spec.function,
        "description": spec.description,
        "example": build_example(spec),
        "params": [_param_row(spec, public) for public in _ordered_public_params(spec)],
        "note": note,
    }


def _ordered_public_params(spec: DatasetSpec) -> list[str]:
    optional = [p for p in spec.public_params() if p not in spec.required]
    return list(spec.required) + optional


def _param_kind(spec: DatasetSpec, public: str) -> str:
    if public in spec.bool_params or (public == "expect_df" and spec.accepts_expect_df):
        return "bool"
    if public in spec.list_params:
        return "list"
    return "scalar"


def _param_row(spec: DatasetSpec, public: str) -> dict[str, Any]:
    return {
        "param": "--" + public.replace("_", "-"),
        "required": public in spec.required,
        "kind": _param_kind(spec, public),
        "说明": param_description(public),
    }


