def _emit_example_arg(public: str, sample: str) -> list[str]:
    """A flag param → `--flag value`; a long-tail param → `--param name=value`.
    Values with shell-special chars are single-quoted so the example stays runnable."""
    special = any(ch in sample for ch in " {}()\"'")
    if public in FLAG_PARAMS:
        return ["--" + public.replace("_", "-"), f"'{sample}'" if special else sample]
    token = f"{public}={sample}"
    return ["--param", f"'{token}'" if special else token]


def build_example(spec: DatasetSpec) -> str:
    """Synthesize a runnable example: `rqq data get <name>` + sample args.

    Uses just the required params (+ a representative id when none are required).
    A date window is NOT auto-added: many datasets return full history from an id
    alone, so a narrow window would wrongly empty them — datasets that genuinely
    need a range carry it in EXAMPLE_OVERRIDES.
    """
    parts = ["rqq data get", spec.name]
    override = EXAMPLE_OVERRIDES.get(spec.name)
    if override is not None:
        for public, sample in override.items():
            parts += _emit_example_arg(public, sample)
        return " ".join(parts)
    for public in spec.required:
        parts += _emit_example_arg(public, _sample_for(spec, public))
    if not spec.required:  # show one representative optional so it's runnable-ish
        if "ids" in spec.param_map:
            parts += _emit_example_arg("ids", _sample_for(spec, "ids"))
        elif "date" in spec.param_map:
            parts += _emit_example_arg("date", sample_value("date"))
        elif "start" in spec.param_map:
            parts += _emit_example_arg("start", sample_value("start"))
            if "end" in spec.param_map:
                parts += _emit_example_arg("end", sample_value("end"))
    return " ".join(parts)


def get_dataset(name: str) -> DatasetSpec:
    try:
        return DATASETS[name]
    except KeyError as exc:
        near = difflib.get_close_matches(name, DATASETS, n=3, cutoff=0.7)
        hint = f"你是不是想输: {', '.join(near)} ？" if near else ""
        raise CliError(
            f"未知数据集 `{name}`。{hint}用 `rqq data list` / `rqq help` 查看全部数据集。"
        ) from exc


def _module_order(category: str) -> int:
    keys = list(MODULES)
    return keys.index(category) if category in keys else len(keys)


def build_dataset_kwargs(spec: DatasetSpec, raw_params: dict[str, Any]) -> dict[str, Any]:
    params = {**{key: value for key, value in raw_params.items() if value is not None}}
    missing = [name for name in spec.required if _is_empty(params.get(name))]
    if missing:
        flags = ", ".join("--" + name.replace("_", "-") for name in missing)
        raise CliError(
            f"`{spec.name}` 缺少必填参数: {flags}\n"
            f"  查看参数: rqq data describe {spec.name}\n"
            f"  可运行示例: {build_example(spec)}"
        )

    kwargs: dict[str, Any] = {}
    for public_name, rq_name in spec.param_map.items():
        if public_name not in params or params[public_name] is None:
            continue
        value = params[public_name]
        if public_name in spec.list_params:
            value = _as_list(value)
            if rq_name == "order_book_id":
                value = value[0] if value else None
            elif public_name == "ids" and len(value) == 1:
                value = value[0]
        if public_name in spec.bool_params:
            value = _as_bool(value)
        if public_name in INT_PARAMS and value is not None:
            value = int(value)
        kwargs[rq_name] = value

    # expect_df: default to a DataFrame; let the user override via
    # --no-expect-df / --param expect_df=false.
    if spec.accepts_expect_df:
        override = params.get("expect_df")
        kwargs["expect_df"] = _as_bool(override) if override is not None else True
    return kwargs


def call_dataset(name: str, raw_params: dict[str, Any], resolver: Callable[[str], Any]) -> Any:
    spec = get_dataset(name)
    target = resolver(spec.function)
    kwargs = build_dataset_kwargs(spec, raw_params)
    return target(**kwargs)


def _as_list(value: Any) -> list[Any]:
    if value is None:
        return []
    if isinstance(value, list):
        return value
    if isinstance(value, tuple):
        return list(value)
    if isinstance(value, str):
        return [item.strip() for item in value.split(",") if item.strip()]
    return [value]


def _as_bool(value: Any) -> bool:
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        lowered = value.strip().lower()
        if lowered in {"1", "true", "yes", "y", "on"}:
            return True
        if lowered in {"0", "false", "no", "n", "off"}:
            return False
    return bool(value)


def _is_empty(value: Any) -> bool:
    return value is None or value == "" or value == []
