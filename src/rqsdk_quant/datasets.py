def _public(rq_name: str) -> str:
    return STD_PUBLIC.get(rq_name, rq_name)


def param_description(public_name: str) -> str:
    info = PARAM_INFO.get(public_name)
    if info:
        return info[0]
    return public_name.replace("_", " ")


def sample_value(public_name: str) -> str:
    info = PARAM_INFO.get(public_name)
    if info and info[1] is not None:
        return info[1]
    return f"<{public_name.replace('_', '-')}>"


@dataclass(frozen=True)
class ParamSnap:
    rq_name: str       # rqdatac parameter name
    required: bool     # no default in the signature
    is_bool: bool      # default is a Python bool


def _parse_params(spec: str) -> tuple[ParamSnap, ...]:
    """Parse a params string like "!order_book_ids start_date expect_df:bool"."""
    snaps = []
    for token in spec.split():
        required = token.startswith("!")
        if required:
            token = token[1:]
        is_bool = token.endswith(":bool")
        if is_bool:
            token = token[: -len(":bool")]
        snaps.append(ParamSnap(token, required, is_bool))
    return tuple(snaps)


@dataclass
class DatasetSpec:
    name: str           # public dataset name (unique, kebab-case)
    function: str       # dotted rqdatac path, e.g. "fund.get_nav"
    category: str       # official module slug (a key of MODULES)
    description: str
    params_spec: str    # signature snapshot, parsed into `params`

    params: tuple[ParamSnap, ...] = field(init=False, default=())
    param_map: dict[str, str] = field(init=False, default_factory=dict)
    required: tuple[str, ...] = field(init=False, default=())
    list_params: tuple[str, ...] = field(init=False, default=())
    bool_params: tuple[str, ...] = field(init=False, default=())
    accepts_expect_df: bool = field(init=False, default=False)

    def __post_init__(self) -> None:
        self.params = _parse_params(self.params_spec)
        # param_map: public name -> rqdatac name. `expect_df` is handled
        # specially (injected as True by default) so it is not auto-mapped.
        self.param_map = {
            _public(p.rq_name): p.rq_name for p in self.params if p.rq_name != "expect_df"
        }
        self.required = tuple(_public(p.rq_name) for p in self.params if p.required)
        self.list_params = tuple(
            _public(p.rq_name) for p in self.params if p.rq_name in LIST_PARAMS
        )
        self.bool_params = tuple(
            _public(p.rq_name) for p in self.params if p.is_bool and p.rq_name != "expect_df"
        )
        self.accepts_expect_df = any(p.rq_name == "expect_df" for p in self.params)

    @property
    def module(self) -> str:
        return MODULES.get(self.category, self.category)

    def public_params(self) -> list[str]:
        params = sorted(self.param_map)
        if self.accepts_expect_df:
            params.append("expect_df")
        return params


